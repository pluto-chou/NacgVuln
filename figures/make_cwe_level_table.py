"""
make_cwe_level_table.py

Purpose
-------
Generate a LocVul-style Top-10 CWE-level localization table from existing
Seq2Seq evaluation outputs. This script does NOT run the model again. It reads
per-seed *_details.csv files, joins each row back to the corresponding test set
so that CWE labels are available, and computes per-CWE line-level metrics.

Expected inputs
---------------
1) Evaluation details CSVs, usually generated by:
   seq2seq_eval_fixed_v3_compat_offline_v3_vulnonly.py
   Required columns in details files:
     row, gt_lines, pred_lines, ranked_topK, A@K, MRR@K, MAP@K, IFA
   P@K and R@K are recomputed from ranked_topK and gt_lines.

2) Either preprocessed test files:
     preprocessed_data_test_0.csv ... preprocessed_data_test_9.csv
   or dataset.csv, from which the fixed last-10% test split is reconstructed.
   Required dataset/test columns:
     processed_func, target, flaw_line, flaw_line_index, CWE ID
   The CWE column can also be named CWE, cwe, cwe_id, cwe_id/name, etc.

Outputs
-------
- cwe_level_per_seed_metrics.csv
- cwe_level_mean_std_metrics.csv
- cwe_level_top10_table.csv
- cwe_level_top10_table.md
- cwe_level_joined_details.csv  (optional, with --save_joined yes)

Example
-------
python make_cwe_level_table.py \
  --eval_outputs /home/tom/deeplearning/LocVul-main/nacgvuln_10seed/eval_outputs \
  --dataset_path /home/tom/deeplearning/LocVul-main/data/dataset.csv \
  --out_dir /home/tom/deeplearning/LocVul-main/paper_extra_results/cwe_level \
  --k 10 \
  --top_n 10
"""

